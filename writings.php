A Eulogy for Animals <br>
Man to beast is the looming figure of danger, <br>
a violater, a killer, all our human virtues and artistry bereft, and merely the hand that grabs, the boot that kicks, the figure that comes for their young, the wielder of  the chain, the blade, the rod, the gun, the milking and maiming apartus, towering figures devoid of mercy, threats to be avoided and likely reviled... And yet, still from behind the bars, the wet forgiving nose, the curious snout comes to nuzzle the hand that rips child from mother. The scope unfathomable, our savagery, immediate and viscerally met, and for all our smiling emojis and words to soothe, a sundered ship, that if driven on will surely founder, we will not fully understand these, the weight of  our actions, until the oblivion we've inflicted on so many  rushes to meet us in the most unwelcome sense. <br>

<br>
